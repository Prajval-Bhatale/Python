marks=int(input("Enter the marks obtained by the students :"))

if marks>90:
    print(f"Student obtained {marks}.Performance is excellent")

elif marks>=80 and marks<90:
    print(f"Student obtained {marks}.Performance is very good")

elif marks>=70 and marks<80:
    print(f"Student obtained {marks}.Performance is  good")

elif marks>=60 and marks<70:
    print(f"Student obtained {marks}.Performance is average")

else :
    print(f"Student obtained {marks}. Performance is poor")





